#include "../includes/so_long.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*c;
	size_t			i;

	i = 0;
	c = s;
	while (i < n)
	{
		*c = 0;
		c++;
		i++;
	}
}
